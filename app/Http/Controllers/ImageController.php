    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit(Image $image)
    {
        return view('pages.edit-image', compact('image'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Image $image)
    {
        $request->validate([
            'slug'=>'required|image|mimes:png,jpg,jpeg|max:5000',
        ]);

        $image->update ([
            'slug' =>$image->slug,
            'created_at'=>now()
        ]);

        return redirect()->route('images.all', $image->id)->with('status', 'Image modifié');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Image $image)
    {
        $image->delete();
        return redirect()->route('images.all')->with('status', 'Image supprimée');
    }
